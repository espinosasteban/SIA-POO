package Fabrica;

public class Llanta {
    private String rin;
    private String cubierta;
    private float tamano;
    private boolean chuzada;

    public Llanta(String rin, String cubierta, float tamano, boolean chuzada) {
        this.rin = rin;
        this.cubierta = cubierta;
        this.tamano = tamano;
        this.chuzada = chuzada;
    }
}
